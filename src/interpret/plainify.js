// ========== 4. 主翻译函数 ==========
export function plainify(text) {
  if (!text || typeof text !== "string") return text;

  let out = text;

  // Step 1: 保护词 → 占位符
  const placeholders = {};
  PROTECTED.forEach((w, i) => {
    const key = `__ZWP${i}__`;
    if (out.includes(w)) {
      placeholders[key] = w;
      out = out.split(w).join(key);
    }
  });

  // Step 2: 术语替换
  for (const [src, dst] of TERM_DICT) {
    out = out.split(src).join(dst);
  }

  // Step 3: 句式改写
  for (const [pattern, replacement] of SENTENCE_RULES) {
    out = out.replace(pattern, replacement);
  }

  // Step 4: 还原保护词
  for (const [key, w] of Object.entries(placeholders)) {
    out = out.split(key).join(w);
  }

  // Step 5: 整理空白
  out = out.replace(/\n{3,}/g, "\n\n").trim();

  return out;
}

// 对解读报告整体翻译（对 report 对象每个字符串字段应用 plainify）
export function plainifyReport(report) {
  const out = {};
  for (const [key, value] of Object.entries(report)) {
    if (typeof value === "string") {
      out[key] = plainify(value);
    } else if (Array.isArray(value)) {
      out[key] = value.map((item) => {
        if (typeof item === "string") return plainify(item);
        if (item && typeof item === "object") {
          const n = {};
          for (const [k, v] of Object.entries(item)) {
            n[k] = typeof v === "string" ? plainify(v) : v;
          }
          return n;
        }
        return item;
      });
    } else if (value && typeof value === "object") {
      const n = {};
      for (const [k, v] of Object.entries(value)) {
        n[k] = typeof v === "string" ? plainify(v) : v;
      }
      out[key] = n;
    } else {
      out[key] = value;
    }
  }
  return out;
}

// 翻译追问结果
export function plainifyAnswer(answer) {
  if (!answer) return answer;
  return {
    ...answer,
    title: plainify(answer.title || ""),
    content: plainify(answer.content || ""),
  };
}
