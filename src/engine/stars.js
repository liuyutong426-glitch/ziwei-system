// ===== 煞星 =====
// 擎羊陀罗：跟随禄存。禄存前一位(顺行)为擎羊，禄存后一位(逆行)为陀罗
export function placeYangTuo(yearGan) {
  const lu = placeLucun(yearGan);
  return {
    "擎羊": (lu + 1) % 12,
    "陀罗": (lu - 1 + 12) % 12,
  };
}

// 火星铃星：按年支 + 时辰
// 标准规则：
//   寅午戌：火星丑宫起子时，铃星卯宫起子时；
//   申子辰：火星寅宫起子时，铃星戌宫起子时；
//   巳酉丑：火星卯宫起子时，铃星戌宫起子时；
//   亥卯未：火星酉宫起子时，铃星戌宫起子时；
// 然后顺数至生时
// 注：传统另有"寅午戌人"的变体；此采用常见版本。
const HUOLING_START = {
  "寅":{火:1,铃:3}, "午":{火:1,铃:3}, "戌":{火:1,铃:3},
  "申":{火:2,铃:10}, "子":{火:2,铃:10}, "辰":{火:2,铃:10},
  "巳":{火:3,铃:10}, "酉":{火:3,铃:10}, "丑":{火:3,铃:10},
  "亥":{火:9,铃:10}, "卯":{火:9,铃:10}, "未":{火:9,铃:10},
};
export function placeHuoLing(yearZhi, hourZhiIdx) {
  const s = HUOLING_START[yearZhi];
  return {
    "火星": (s["火"] + hourZhiIdx) % 12,
    "铃星": (s["铃"] + hourZhiIdx) % 12,
  };
}

// 地空地劫：亥宫起子时，地劫顺行，地空逆行
export function placeKongJie(hourZhiIdx) {
  return {
    "地劫": (11 + hourZhiIdx) % 12,
    "地空": (11 - hourZhiIdx + 12) % 12,
  };
}

